export function smileyFromKey(key: number): SmileyData {
  let smiley;
  let smileyText;
  switch (key) {
    case Smiley.Elite:
      smiley = EliteSmiley();
      smileyText = i18n.t('smileyText.elite')
      break;
    case Smiley.Bad:
      smiley = SmileySad();
      smileyText = i18n.t('smileyText.bad')
      break;
    case Smiley.Decent:
      smiley = SmileyOkay();
      smileyText = i18n.t('smileyText.decent')
      break;
    case Smiley.Good:
      smiley = SmileyHappy();
      smileyText = i18n.t('smileyText.good')
      break;
    case Smiley.Neutral:
      smiley = SmileyNeutral();
      smileyText = i18n.t('smileyText.neutral')
      break;
    default:
      smiley = SmileyHappy();
      smileyText = i18n.t('smileyText.good')
      break;
  }
  return {
    smiley: smiley,
    smileyText: smileyText
  };
}

